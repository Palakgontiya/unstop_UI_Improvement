/**
 * step 1 we need to have different data for tech and non tech
 * step 2 if tech is selected then we will ony search in tech segment
 * step 3 we will take the search term and try to find it in  
 * JobID
Title
Org Name
Tags (Name)
Seo Description & Keywords
Regn Requirement -> Hiring
Competition -> Responsibilities, Tech Skills, Short Desc
if the term is present in any of these fields then we will consider that record
we need to repeat the same process for all the records and showcase the matched records to the user

so we can iterate through the data array and in each record we will use regex to match the search term, if it matches then we will take that record using its index and 
store it in a new array and show the currentdata to the user or may be we can just store the indexes of the matched record in s string saperated with comma and use those indexes to show the data to the user
 */

function findMatchingJob({ search, segment }) {
  const isTechSegment = segment === 'technical';

  const fieldsToSearch = [
    'id',
    'title',
    'organisation.name',
    'tags.name',
    'seo_details.description',
    'seo_details.keywords',
    'regnRequirements.hiring',
    'competition.responsibilities',
    'competition.short_desc',
  ];

  if (isTechSegment) {
    fieldsToSearch.push('competition.technical_skills');
  }

  const matchedIndexes = data
    .map((item, index) => ({
      index,
      isMatched: isTechSegment
        ? // Search only in technical segment fields
          fieldsToSearch.some(field => {
            const fieldValue = field.split('.').reduce((obj, key) => (obj && obj[key] !== undefined ? obj[key] : undefined), item);

            if (Array.isArray(fieldValue)) {
              return fieldValue.some(value => String(value).toLowerCase().includes(search.toLowerCase()));
            }

            return fieldValue && String(fieldValue).toLowerCase().includes(search.toLowerCase());
          })
        : // Search only in non-technical segment fields
          fieldsToSearch
            .filter(field => !field.includes('technical'))
            .some(field => {
              const fieldValue = field.split('.').reduce((obj, key) => (obj && obj[key] !== undefined ? obj[key] : undefined), item);
              return fieldValue && String(fieldValue).toLowerCase().includes(search.toLowerCase());
            }),
    }))
    .filter(({ isMatched }) => isMatched)
    .map(({ index }) => index);

  const matchedItems = matchedIndexes.map(index => data[index]);

  return matchedItems;
}

